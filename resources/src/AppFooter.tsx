import {Link, Text} from "@geist-ui/react"
import GitInfo from "react-git-info/macro"

const AppFooter = () => {
    const gitInfo = GitInfo()
    const version = gitInfo.tags.length > 0 ? gitInfo.tags[0] : gitInfo.commit.shortHash
    const commitUrl = 'https://github.com/LukWebsForge/TldrProgress/commit/' + gitInfo.commit.hash

    return <Text p>
        Generated by <Link href='https://github.com/LukWebsForge/TldrProgress' icon target='_blank'>
        tldr-translation-progress </Link> •
        Version <Link href={commitUrl} target='_blank'>{version}</Link> •
        Thanks for using this site
    </Text>
}

export {AppFooter}
